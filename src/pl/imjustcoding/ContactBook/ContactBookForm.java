    private void jButton_EditContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EditContactActionPerformed

        if(verifData()){
            int id=Integer.valueOf(jTextField_Id.getText());
            String fName=jTextField_FName.getText();
            String lName=jTextField_LName.getText();
            String phone=jTextField_Phone.getText();
            String email=jTextField_Email.getText();
            String address=jTextArea_Address.getText();
            String group=jComboBox_Group.getSelectedItem().toString();

            if(imagePth!=null){
                byte[] img=null;
                try {
                    Path pth=Paths.get(imagePth);
                    img = Files.readAllBytes(pth);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(ContactBookForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(ContactBookForm.class.getName()).log(Level.SEVERE, null, ex);
                }

                Contact c=new Contact(id, fName, lName, group, phone, email, address, img, currentUserId);
                ContactQuery cq=new ContactQuery();
                cq.updateContact(c, true);

                jTable1.setModel(new DefaultTableModel());
                populateJTable();
                setTableProperties();
                clearFields();
            }else{
                Contact c=new Contact(id, fName, lName, group, phone, email, address, null, currentUserId);
                ContactQuery cq=new ContactQuery();
                cq.updateContact(c, false);

                jTable1.setModel(new DefaultTableModel());
                populateJTable();
                setTableProperties();
                clearFields();
            }
        }   
    }//GEN-LAST:event_jButton_EditContactActionPerformed

    private void jButton_DeleteContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_DeleteContactActionPerformed
       
        
        if(!jTextField_Id.getText().equals("")){
            Integer id=Integer.valueOf(jTextField_Id.getText());
            ContactQuery cq=new ContactQuery();
            cq.deleteContact(id);

            jTable1.setModel(new DefaultTableModel());
            populateJTable();
            setTableProperties();
            clearFields();
        }else{
            JOptionPane.showMessageDialog(null,"Choose some contact to delete");
        }
        
            
    }//GEN-LAST:event_jButton_DeleteContactActionPerformed

