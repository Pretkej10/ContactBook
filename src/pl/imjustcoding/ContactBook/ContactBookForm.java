    private void jButton_AddContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AddContactActionPerformed
        
        if(verifData()){
            String fName=jTextField_FName.getText();
            String lName=jTextField_LName.getText();
            String phone=jTextField_Phone.getText();
            String email=jTextField_Email.getText();
            String address=jTextArea_Address.getText();
            String group=jComboBox_Group.getSelectedItem().toString();
            
            if(imagePth!=null){
                byte[] img=null;
                try {
                    Path pth=Paths.get(imagePth);
                    img = Files.readAllBytes(pth);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(ContactBookForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(ContactBookForm.class.getName()).log(Level.SEVERE, null, ex);
                }

                Contact c=new Contact(null, fName, lName, group, phone, email, address, img, currentUserId);
                ContactQuery cq=new ContactQuery();
                cq.insertContact(c);

                jTable1.setModel(new DefaultTableModel());
                populateJTable();
                setTableProperties();
                clearFields();
            }else{
                JOptionPane.showMessageDialog(null, "Choose a picture, please!");
            }
            
            
        }
    }//GEN-LAST:event_jButton_AddContactActionPerformed

